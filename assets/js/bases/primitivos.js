//string type
let nombre = 'Peter Parker';
console.log(nombre);

nombre = 'Ben Parker';
console.log(nombre);

nombre = "Tia May";

console.log(typeof nombre);

nombre = 123;
console.log(typeof nombre);

//Boolean Value
let esMarvel = false;
console.log(typeof esMarvel);

//Number
let edad = 33;
console.log(typeof edad);

edad = 33.001;
console.log(typeof edad);

//Undefined, it means that the variable doesn't have any value assigned
let superPoder;
console.log(typeof superPoder);

//null
let soyNull = null;
console.log(typeof soyNull);

